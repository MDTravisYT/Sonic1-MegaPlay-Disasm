PAUSEGAME:
		RTS
	;	CODE BEYOND HERE IS UNUSED, BUT REMAINS UNCHANGED FROM THE ORIGINAL GAME
		NOP
		TST.B	(V_LIVES).W
		BEQ.S	UNPAUSE
		TST.W	(F_PAUSE).W
		BNE.S	PAUSE_STOPGAME
		BTST	#7,	(V_JPADHOLD1+1).W
		BEQ.S	PAUSE_DONOTHING
		
	PAUSE_STOPGAME:
		MOVE.W	#1,	(F_PAUSE).W
		MOVE.B	#1,	(F_PAUSEMUSIC).W
		
	PAUSE_LOOP:
		MOVE.B	#$10,	(V_VBLA_ROUTINE).W
		BSR.W	WAITFORVBLA
		TST.B	(F_SLOMOCHEAT).W
		BEQ.S	PAUSE_CHKSTART
		BTST	#6,	(V_JPADHOLD1+1).W
		BEQ.S	PAUSE_CHKBC
		MOVE.B	#4,	(V_GAMEMODE).W
		NOP
		BRA.S	PAUSE_ENDMUSIC
		
	PAUSE_CHKBC:
		BTST	#4,	(V_JPADHOLD1).W
		BNE.S	PAUSE_SLOWMO
		BTST	#5,	(V_JPADHOLD1+1).W
		BNE.S	PAUSE_SLOWMO
		
	PAUSE_CHKSTART:
		BTST	#7,	(V_JPADHOLD1+1).W
		BEQ.S	PAUSE_LOOP
		
	PAUSE_ENDMUSIC:
		MOVE.B	#$80,	(F_PAUSEMUSIC).W
		
	UNPAUSE:
		MOVE.W	#0,	(F_PAUSE).W
		
	PAUSE_DONOTHING:
		RTS
		
	PAUSE_SLOWMO:
		MOVE.W	#1,	(F_PAUSE).W
		MOVE.B	#$80,	(F_PAUSEMUSIC).W
		RTS