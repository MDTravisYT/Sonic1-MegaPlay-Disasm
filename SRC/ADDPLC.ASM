ADDPLC:
		MOVEM.L	A1-A2,	-(SP)
		LEA		(ARTLOADCUES).L,	A1
		ADD.W	D0,	D0
		MOVE.W	(A1,D0.W),	D0
		LEA		(A1,D0.W),	A1
		LEA		(V_PLC_BUFFER).W,	A2
		
	ADDPLC_FINDSPACE:
		TST.L	(A2)
		BEQ.S	ADDPLC_COPYTORAM
		ADDQ.W	#6,	A2
		BRA.S	ADDPLC_FINDSPACE
		
	ADDPLC_COPYTORAM:
		MOVE.W	(A1)+,	D0
		BMI.S	ADDPLC_SKIP
		
	ADDPLC_LOOP:
		MOVE.L	(A1)+,	(A2)+
		MOVE.W	(A1)+,	(A2)+
		DBF		D0,	ADDPLC_LOOP
		
	ADDPLC_SKIP:
		MOVEM.L	(SP)+,	A1-A2
		RTS