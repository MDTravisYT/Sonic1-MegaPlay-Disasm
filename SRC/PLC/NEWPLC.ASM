NEWPLC:
		MOVEM.L	A1-A2,	-(SP)
		LEA		(ARTLOADCUES).L,	A1
		ADD.W	D0,	D0
		MOVE.W	(A1,D0.W),	D0
		LEA		(A1,D0.W),	A1
		BSR.S	CLEARPLC
		LEA		(V_PLC_BUFFER).W,	A2
		MOVE.W	(A1)+,	D0
		BMI.S	NEWPLC_SKIP
		
	NEWPLC_LOOP:
		MOVE.L	(A1)+,	(A2)+
		MOVE.W	(A1)+,	(A2)+
		DBF		D0,	NEWPLC_LOOP
		
	NEWPLC_SKIP:
		MOVEM.L	(SP)+,	A1-A2
		RTS