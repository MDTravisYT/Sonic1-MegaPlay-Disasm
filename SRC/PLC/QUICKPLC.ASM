QUICKPLC:
		LEA		(ARTLOADCUES).L,	A1
		ADD.W	D0,	D0
		MOVE.W	(A1,D0.W),	D0
		LEA		(A1,D0.W),	A1
		MOVE.W	(A1)+,	D1
		
	QPLC_LOOP:
		MOVEA.L	(A1)+,	A0
		MOVEQ	#0,	D0
		MOVE.W	(A1)+,	D0
		LSL.L	#2,	D0
		LSR.W	#2,	D0
		ORI.W	#$4000,	D0
		SWAP	D0
		MOVE.L	D0,	($C00004).L
		BSR.W	NEMDEC
		DBF	D1,	QPLC_LOOP
		RTS