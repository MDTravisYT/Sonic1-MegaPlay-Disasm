PALETTECYCLE:
		MOVEQ	#0,	D2
		MOVEQ	#0,	D0
		MOVE.B	(V_ZONE).W,	D0
		ADD.W	D0,	D0
		MOVE.W	PCYCLE_INDEX(PC,D0.W),	D0
		JMP		PCYCLE_INDEX(PC,D0.W)
		
PCYCLE_INDEX:
		DC.W	PCYCLE_GHZ-PCYCLE_INDEX
		DC.W	PCYCLE_LZ-PCYCLE_INDEX
		DC.W	PCYCLE_MZ-PCYCLE_INDEX
		DC.W	PCYCLE_SLZ-PCYCLE_INDEX
		DC.W	PCYCLE_SYZ-PCYCLE_INDEX
		DC.W	PCYCLE_SBZ-PCYCLE_INDEX
		
PCYCLE_TITLE:
		LEA		(PAL_TITLECYC).L,	A0
		BRA.S	PCYCGHZ_GO
		
PCYCLE_HISCORE:
		LEA		(PAL_HISCORECYC).L,	A0
		BRA.S	PCYCGHZ_GO
		
PCYCLE_GHZ:
		LEA		(PAL_GHZCYC).L,	A0
		
	PCYCGHZ_GO:
		SUBQ.W	#1,	(V_PCYC_TIME).W
		BPL.S	PCYCGHZ_SKIP
		MOVE.W	#5,	(V_PCYC_TIME).W
		MOVE.W	(V_PCYC_NUM).W,	D0
		ADDQ.W	#1,	(V_PCYC_NUM).W
		ANDI.W	#3,	D0
		LSL.W	#3,	D0
		LEA		(V_PAL_DRY+$50).W,	A1
		MOVE.L	(A0,D0.W),	(A1)+
		MOVE.L	4(A0,D0.W),	(A1)
		
	PCYCGHZ_SKIP:
		RTS
		
PCYCLE_LZ:
		SUBQ.W	#1,	(V_PCYC_TIME).W
		BPL.S	PCYCLZ_SKIP1
		MOVE.W	#2,	(V_PCYC_TIME).W
		MOVE.W	(V_PCYC_NUM).W,	D0
		ADDQ.W	#1,	(V_PCYC_NUM).W
		ANDI.W	#3,	D0
		LSL.W	#3,	D0
		LEA		(PAL_LZCYC1).L,	A0
		CMPI.B	#3,	(V_ZONE+1).W
		BNE.S	PCYCLZ_NOTSBZ3
		LEA		(PAL_SBC3CYC).L,	A0
		
	PCYCLZ_NOTSBZ3:
		LEA		(V_PAL_DRY+$56).W,	A1
		MOVE.L	(A0,D0.W),	(A1)+
		MOVE.L	4(A0,D0.W),	(A1)
		LEA		(V_PAL_WATER+$56).W,	A1
		MOVE.L	(A0,D0.W),	(A1)+
		MOVE.L	4(A0,D0.W),	(A1)
		
	PCYCLZ_SKIP1:
		MOVE.W	(V_FRAMECOUNT).W,	D0
		ANDI.W	#7,	D0
		MOVE.B	PCYCLZ_SEQ(PC,D0.W),	D0
		BEQ.S	PCYCLZ_SKIP2
		MOVEQ	#1,	D1
		TST.B	(F_CONVEYREV).W
		BEQ.S	PCYCLZ_SKIP2
		MOVEQ	#1,	D1
		TST.B	(F_CONVEYREV).W
		BEQ.S	PCYCLZ_NOREV
		NEG.W	D1
		
	PCYCLZ_NOREV:
		MOVE.W	(V_PAL_BUFFER).W,	D0
		ANDI.W	#3,	D0
		ADD.W	D1,	D0
		CMPI.W	#3,	D0
		BCS.S	LOC_1C32
		MOVE.W	D0,	D1
		MOVEQ	#0,	D0
		TST.W	D1
		BPL.S	LOC_1C32
		MOVEQ	#2,	D0
		
	LOC_1C32:
		MOVE.W	D0,	(V_PAL_BUFFER).W
		ADD.W	D0,	D0
		MOVE.W	D0,	D1
		ADD.W	D0,	D0
		ADD.W	D1,	D0
		LEA		(PAL_LZCYC2).L,	A0
		LEA		(V_PAL_DRY+$76).W,	A1
		MOVE.L	(A0,D0.W),	(A1)+
		MOVE.W	4(A0,D0.W),	(A1)
		LEA		(PAL_LZCYC3).L,	A0
		LEA		(V_PAL_WATER+$76).W,	A1
		MOVE.L	(A0,D0.W),	(A1)+
		MOVE.W	4(A0,D0.W),	(A1)
		
	PCYCLZ_SKIP2:
		RTS
	
